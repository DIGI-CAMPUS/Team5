SELECT *
FROM emp e JOIN DEPT D
ON (e.DEPTNO = d.DEPTNO)
ORDER BY EMPNO ;

SELECT *
FROM emp e JOIN dept D using(DEPTNO)
ORDER BY e.EMPNO ;

